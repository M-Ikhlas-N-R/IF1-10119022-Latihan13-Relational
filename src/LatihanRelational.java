public class LatihanRelational {
    public static void main(String[] args) {
        int bil1 = 10;
        int bil2 = 20;
        System.out.println("a == b = " + (bil1 == bil2));
        System.out.println("a != b = " + (bil1 != bil2));
        System.out.println("a > b = " + (bil1 > bil2));
        System.out.println("a < b = " + (bil1 < bil2));
        System.out.println("a >= b = " + (bil1 >= bil2));
        System.out.println("a <= b = " + (bil1 <= bil2));
    }
}
